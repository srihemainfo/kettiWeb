     let currentIndex = 0;
    const images = ['images/women.webp', 'images/women.webp', 'images/women.webp', 'images/women.webp', 'images/women.webp'];

    function updateImageCounter() {
        document.getElementById('imageCounter').innerText = `${currentIndex + 1}/${images.length}`;
    }

    function switchImage(imageSrc, index) {
        document.getElementById('mainImage').src = imageSrc;
        currentIndex = index;
        updateImageCounter();

        const thumbnails = document.getElementsByClassName('thumbnail');
        for (let i = 0; i < thumbnails.length; i++) {
            thumbnails[i].classList.remove('active');
        }
        thumbnails[currentIndex].classList.add('active');
    }

    function showNextImage() {
        currentIndex = (currentIndex + 1) % images.length;
        switchImage(images[currentIndex], currentIndex);
    }

    function showPrevImage() {
        currentIndex = (currentIndex - 1 + images.length) % images.length;
        switchImage(images[currentIndex], currentIndex);
    }

    // Initial counter update
    updateImageCounter();
    
